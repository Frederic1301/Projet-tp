        <h4>L'ENTRAINEMENT AU SEUIL</h4>

        <p>La résistance est un autre élément fondamental en trail : il s’agit de s’entraîner à fournir
            un effort assez important le plus longtemps possible. « L’intérêt est de développer une
            capacité à maintenir un rendement élevé – plus important qu’en endurance – sur une durée
            relativement longue, correspondant aux distances en trail », explique notre expert.

            Pour cela, il faudra s’entraîner grâce à des séances spécifiques basées sur le temps
            d’effort et sur la gestion de l’allure. Comme pour la route, l’entraînement se compose de
            plusieurs fractions, « des répétitions comprises entre 6 et 10 minutes d’effort» et
            forcément plus longues que pour le fractionné court La séance est à réaliser prioritairement
            en milieu naturel. « Je recommande toujours de réaliser ce type de séances sur des parcours
            proches de ceux rencontrés en compétition. »

            Cet entraînement spécifique peut intervenir après un cycle VMA pour améliorer l’adaptation
            de l’organisme et habituer le traileur à fournir un effort soutenu sur tous types de
            parcours ( côtes, plat, obstacles). Pour les coureurs utilisant un cardiofréquencemètre,
            l’intensité sera comprise entre 80 % et 90 % de la fréquence cardiaque maximale (FCM).
            On peut tout aussi bien faire de l’isométrique et de l’excentrique pour renforcer le muscle
            en l’allongeant. »</p>
        <p>LA SORTIE LONGUE
            En trail comme pour la course sur route, l’endurance est la base de tout, « la qualité la
            plus fondamentale à développer », selon Manu Gault, traileur du teamAsics, plusieurs fois
            vainqueur de l’Éco-Trail de Paris. « S’entrainer à courir longtemps est une nécessité pour
            tout type de préparation. » Une bonne endurance permet de mieux gérer la fatigue
            et d’habituer l’organisme à la casse musculaire, surtout sur du très long, comme en trail.
            Cette endurance fondamentale se construit grâce à un entraînement équilibré.
            « L’enchainement des séances de résistance (seuil), de vitesse et de dénivelé va avoir une
            influence sur le développement global de toute la filière aérobie et donc sur l’endurance. »
            La sortie longue va permettre à l’organisme de mieux recycler les lipides comme carburant de
            l’effort, « c’est aussi pour cela que la sortie longue commence habituellement autour de 2
            heures de course ».</p>

        <p>LE RENFORCEMENT MUSCULAIRE
            « C’est le deuxième incontournable de la préparation : le trail exige des muscles puissants
            et résistants pour aborder de longues distances et des dénivelés importants. » Pour cela,
            il faut planifier au minimum deux séances hebdomadaires de PPG sur plusieurs semaines. Les
            exercices concernent le gainage abdominal, pour le maintien et la position de course, « mais
            aussi la force musculaire, grâce à un travail pliométrique sur les jambes pour développer un
            mouvement puissant et rapide».
            Des exercices de multibonds, des pas chassés et des éducatifs plus spécialisés vont
            permettre de bien travailler ses muscles, la séance étant plus centrée sur des exercices
            d’explosivité et de vitesse d’exécution. « L’idéal est de varier le plus possible les
            exercices de renforcement.</p>

        <p>LE DÉNIVELÉ
            L’enchaînement de montées et de descentes constitue la spécificité de la course nature, une
            qualité qui se travaille grâce à des exercices techniques dédiés (en descente) et à un
            entraînement adapté (en montée). Le plus important est de ne pas négliger les descentes au
            profit d’un travail uniquement basé sur la montée de côtes. « Ce type d’entraînement est
            tout à fait réalisable pendant la sortie longue, cela permet de reproduire les conditions de
            course et de s’habituer à l’effort qui est demandé durant un trail. »
            Un exercice courant consiste à enchaîner une série de côtes en intensité et de profiter de
            la descente pour gagner en assurance et en technique. « On peut faire des séries de 30
            secondes x 30 secondes en côtes, mais attention car la séance est plutôt réservée aux bons
            traileurs. » Pour travailler le dénivelé, le principe de progressivité dans les exercices
            est primordial; commencez par de petites côtes pas trop techniques et, au fur et à mesure
            des séances ( et des sensations), passez sur des dénivelés plus importants.
            Pour diversifier et progresser plus rapidement, le conseil est de varier les longueurs de
            côtes, les pourcentages et les vitesses pour balaye</p>
