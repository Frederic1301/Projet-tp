        <h4>UN NÉCESSAIRE TRAVAIL DE VMA</h4>
        <p>
            La VMA (Vitesse Maximale Aérobie) est la vitesse pour laquelle la consommation d’oxygène
            est maximale. C’est une vitesse qu’on peut maintenir entre 4min pour les débutants et
            8min pour les coureurs élites.
            <br /><br />
            C’est la vitesse à laquelle on atteint la VO2 max.
            <br /><br />
            La VMA est un des indicateurs de performance en course à pied. Il est important de la
            connaître car c’est à partir de cette VMA qu’on va pouvoir définir les allures de
            travail et ainsi améliorer ses performances.
            A partir de cell-ci, on va lui appliquer des pourcentage pour calculer les différentes
            allures d’entraînement. Pour autant, une fois ces allures d’entraînement identifiées,
            tout n’est pas terminé.
            Il faut aussi savoir les utiliser et les intégrer dans un programme d’entraînement, en
            fonction de l’objectif défini au préalable.
            <br /><br />
            Le passage par la piste d’athlétisme n’est pas toujours obligé pour les traileurs, tant
            l’exercice peut rebuter et sembler réservé aux coureurs sur route, plus habitués à
            travailler ces distances normées.
            <br /><br />
            « C’est une erreur, corrige notre expert, le fractionné court, sur 400 ou 500 mètres,
            est aussi très intéressant – et même nécessaire pour les traileurs -, peu importe le
            niveau et l’objectif préparé. »
            <br /><br />
            Un travail de vitesse d’autant plus fondamental que « les sorties longues et les courses sur
            de grandes distances ont tendance à nous faire perdre de la vitesse de base, une vitesse
            pourtant indispensable pour aller plus vite et durer plus longtemps».
            <br /><br />
            Mais l’entraînement en fractionné aura également d’autres effets bénéfiques « Ces séances
            font aussi travailler les qualités de pied et l’économie de course. » Idéalement, un
            travail spécifique en VMA est à planifier « sous forme de cycle de quelques semaines»
            avant de commencer une préparation à une course. Cela vous permettra de jouer sur toutes
            les filières ( aérobie et anaérobie) avant de vous lancer dans un entraînement plus axé
            sur le profil du trail préparé.
            <br /><br />
            En cas d’aversion profonde pour la piste d’athlétisme, rien ne vous empêche de fractionner.
            En effet, il est tout à fait possible et même conseillé de faire du fractionné en forêt !
