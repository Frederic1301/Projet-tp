        <h5 id="training">Le fractionné :</h5>
        <p>
            Beaucoup de coureurs ne fractionne pas et ont même une aversion certaine pour la piste.
            Pourtant, fractionner est indispensable si l'on veut progresser.
            De plus, fractionner permet de travailler sa foulée, son contact au sol, savoir garder
            un rhytme régulier soutenu,...
            Une des craintes des débutants qui veulent progresser, c’est comment débuter le
            fractionné ! Car le fractionné en course à pied, c’est un domaine qui fait peur. Ce sont
            des séances réputées difficiles où l’on sort de sa zone de confort…
        </p>
        <p>
            L’entraînement fractionné, c’est tout simplement le fait de s’entraîner en alternant des
            phases rapides et des phases de récupération. C’est un travail qualitatif qui permet de
            travailler à des vitesses élevées pendant une longue période de temps.
            L’entraînement fractionné permet d’enchaîner ces vitesses élevées pendant beaucoup plus
            longtemps qu’avec la course en continu et donc de progresser plus.
            Le coeur un gros muscle, très endurant. Il faut donc le pousser pendant un certain temps
            avant qu’il ne se renforce. Surtout si vous êtes un coureur relativement expérimenté !
            Votre coeur a l’habitude de fonctionner à haut régime, il vous en faudra beaucoup pour
            l’impressionner et qu’il se renforce encore !
            L’entraînement fractionné permet de travailler à une haut niveau d’intensité sans pour
            autant finir sur les rotules à la fin de chaque fraction !
        </p>
        <p>
            Pourcentages de VMA, de FCM, seuil lactique… Oubliez tout ceci pour commencer ! Pour
            savoir comment débuter le fractionné simplement, on va y aller avec vos sensations.
            Donner-vous un objectifs suivant vos capacités (par exemple, 6*500 en 1mn45 ou plus avec
            30 secondes de récupération entre chaque séries et vous pourrez diminuer ce temps
            suivant votre progression)
        </p>
        <p>
            Autre exemple : Imaginez une séance de 15x200m recup 30sec, une séance que vous courez à
            une allure que vous tiendriez grand maximum 2km en continu. Et bien en faisant un
            fractionné de 15x200m, vous allez parcourir 3000m au total à votre allure 2000m !
            Vous voyez le truc ? Votre coeur va fonctionner à allure 2000m pendant plus longtemps
            qu’il ne serait capable de le faire en continu.
            Il va donc essayer de s’adapter à cet effort et vous allez progresser encore plus !
        </p>

